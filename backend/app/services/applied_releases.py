"""Persistent tracking of Claude Code releases the user has adopted.

"Applying" a release from the What's New panel is a user-visible checkpoint:
the release is recorded on disk so it stays marked as handled across restarts,
and a human-readable markdown log is kept alongside it so the maintainer can
see at a glance which releases have been incorporated and what they changed.

This service is deliberately narrow — it only tracks state. It does NOT modify
scaffolder schemas, templates, or form options; that's a separate, human-reviewed
task. The log gives the maintainer a checklist to work from.
"""
from __future__ import annotations

import json
import logging
import time
from pathlib import Path
from typing import Any, Iterable

from filelock import FileLock

log = logging.getLogger(__name__)

_BACKEND_ROOT = Path(__file__).resolve().parent.parent.parent
STORE = _BACKEND_ROOT / ".applied_releases.json"
LOG_FILE = _BACKEND_ROOT / "APPLIED_RELEASES.md"
_LOCK = FileLock(str(STORE) + ".lock")

_RELEVANCE_ORDER = {"high": 0, "medium": 1, "low": 2, "none": 3}


def _load() -> dict[str, Any]:
    if not STORE.exists():
        return {"applied": []}
    try:
        data = json.loads(STORE.read_text(encoding="utf-8"))
    except Exception as exc:
        log.warning("Could not read %s: %s — starting fresh", STORE, exc)
        return {"applied": []}
    if isinstance(data, dict) and isinstance(data.get("applied"), list):
        return data
    return {"applied": []}


def _save(data: dict[str, Any]) -> None:
    try:
        STORE.write_text(
            json.dumps(data, indent=2, ensure_ascii=False) + "\n",
            encoding="utf-8",
        )
    except Exception as exc:
        log.warning("Could not write %s: %s", STORE, exc)


def applied_dates() -> set[str]:
    """Identifiers of every release currently marked as applied."""
    return {entry["date"] for entry in _load()["applied"] if "date" in entry}


def applied_list() -> list[dict[str, Any]]:
    """Applied releases, newest-first by application time."""
    entries = _load()["applied"]
    return sorted(entries, key=lambda e: e.get("applied_at", 0.0), reverse=True)


def apply_release(
    date: str,
    items: list[dict[str, Any]],
    content_hash: str,
    note: str = "",
) -> dict[str, Any]:
    """Mark a release as applied. Re-applying the same date replaces the prior entry."""
    entry = {
        "date": date,
        "applied_at": time.time(),
        "content_hash": content_hash,
        "items": items,
        "note": note.strip(),
    }
    with _LOCK:
        data = _load()
        data["applied"] = [e for e in data["applied"] if e.get("date") != date]
        data["applied"].insert(0, entry)
        _save(data)
        _regenerate_log(data["applied"])
    return entry


def unapply_release(date: str) -> bool:
    """Remove a release from the applied list. Returns True if something was removed."""
    with _LOCK:
        data = _load()
        before = len(data["applied"])
        data["applied"] = [e for e in data["applied"] if e.get("date") != date]
        if len(data["applied"]) == before:
            return False
        _save(data)
        _regenerate_log(data["applied"])
    return True


def _regenerate_log(entries: Iterable[dict[str, Any]]) -> None:
    """Rewrite the human-readable markdown log from the full applied list."""
    entries = list(entries)
    if not entries:
        LOG_FILE.write_text(
            "# Applied Claude Code Releases\n\n"
            "_Releases adopted via the What's New panel. None yet._\n",
            encoding="utf-8",
        )
        return

    lines: list[str] = [
        "# Applied Claude Code Releases",
        "",
        "_Generated by the What's New panel — do not edit by hand._",
        "",
    ]
    for entry in entries:
        applied_at = entry.get("applied_at", 0.0)
        ts = time.strftime("%Y-%m-%d %H:%M", time.localtime(applied_at))
        lines.append(f"## Release {entry.get('date', '?')}")
        lines.append("")
        lines.append(f"_applied {ts} \u00B7 source hash `{entry.get('content_hash', '?')}`_")
        note = entry.get("note", "")
        if note:
            lines.append("")
            for note_line in note.splitlines():
                lines.append(f"> {note_line}")
        lines.append("")

        items = [i for i in entry.get("items", []) if i.get("relevance") != "none"]
        items.sort(key=lambda i: _RELEVANCE_ORDER.get(i.get("relevance", "low"), 9))
        if not items:
            lines.append("_No relevant items in this release._")
            lines.append("")
            continue

        for item in items:
            relevance = item.get("relevance", "?")
            kind = item.get("kind", "other")
            text = (item.get("text") or "").strip()
            affects = ", ".join(item.get("affects") or [])
            summary = (item.get("summary") or "").strip()
            lines.append(f"- **[{relevance}]** _{kind}_ \u2014 {text}")
            if summary:
                lines.append(f"  - {summary}")
            if affects:
                lines.append(f"  - affects: `{affects}`")
        lines.append("")

    try:
        LOG_FILE.write_text("\n".join(lines), encoding="utf-8")
    except Exception as exc:
        log.warning("Could not write %s: %s", LOG_FILE, exc)
